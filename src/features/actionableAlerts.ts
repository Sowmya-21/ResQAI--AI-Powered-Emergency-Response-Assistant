  public async createActionableAlert(
    type: ActionableAlert['type'],
    priority: ActionableAlert['priority'],
    title: string,
    message: string,
    description: string,
    actions: Action[],
    context?: Partial<ActionableAlert['context']>,
    metadata?: Partial<ActionableAlert['metadata']>
  ): Promise<string> {
    const alertId = `actionable_alert_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
    
    const alert: ActionableAlert = {
      id: alertId,
      type,
      priority,
      title,
      message,
      description,
      actions: this.validateActions(actions),
      context: {
        userId: 'current_user',
        sessionId: this.getSessionId(),
        ...context
      },
      time: {
        timestamp: Date.now(),
        urgency: this.determineUrgency(priority),
        deadline: this.calculateDeadline(priority)
      },
      status: {
        state: 'pending',
        createdAt: Date.now(),
        escalationLevel: 1
      },
      metadata: {
        category: this.determineCategory(type),
        tags: this.generateTags(type, priority),
        severity: this.calculateSeverity(priority),
        confidence: 90,
        source: 'system',
        autoGenerated: true,
        requiresConfirmation: priority === 'critical' || priority === 'high',
        ...metadata
      }
    };

    this.alerts.set(alertId, alert);
    this.saveAlerts();
    this.emit('actionableAlertCreated', alert);

    return alertId;
  }

  private validateActions(actions: Action[]): Action[] {
    return actions.filter(action => {
      return action.id && action.title && action.type && action.method;
    });
  }

  private determineUrgency(priority: ActionableAlert['priority']): ActionableAlert['time']['urgency'] {
    switch (priority) {
      case 'critical': return 'immediate';
      case 'high': return 'urgent';
      case 'medium': return 'normal';
      case 'low': return 'low';
      case 'info': return 'low';
      default: return 'normal';
    }
  }

  private calculateDeadline(priority: ActionableAlert['priority']): number {
    switch (priority) {
      case 'critical': return Date.now() + (5 * 60 * 1000); // 5 minutes
      case 'high': return Date.now() + (15 * 60 * 1000); // 15 minutes
      case 'medium': return Date.now() + (60 * 60 * 1000); // 1 hour
      case 'low': return Date.now() + (4 * 60 * 60 * 1000); // 4 hours
      case 'info': return Date.now() + (24 * 60 * 1000); // 24 hours
      default: return Date.now() + (2 * 60 * 60 * 1000); // 2 hours
    }
  }

  private determineCategory(type: ActionableAlert['type']): string {
    switch (type) {
      case 'emergency': return 'emergency_response';
      case 'safety': return 'safety_monitoring';
      case 'health': return 'health_management';
      case 'security': return 'security_incident';
      case 'system': return 'system_notification';
      case 'user_action': return 'user_activity';
      case 'reminder': return 'reminder_management';
      default: return 'general';
    }
  }

  private generateTags(type: ActionableAlert['type'], priority: ActionableAlert['priority']): string[] {
    const tags = [type, priority];
    
    if (type === 'emergency') {
      tags.push('immediate_action', 'auto_workflow');
    }
    
    if (priority === 'critical') {
      tags.push('requires_attention', 'auto_escalate');
    }

    return tags;
  }

  private calculateSeverity(priority: ActionableAlert['priority']): number {
    switch (priority) {
      case 'critical': return 100;
      case 'high': return 80;
      case 'medium': return 60;
      case 'low': return 40;
      case 'info': return 20;
      default: return 50;
    }
  }

  private getSessionId(): string {
    return `session_${Date.now()}`;
  }

  private async processPendingAlerts(): Promise<void> {
    const pendingAlerts = Array.from(this.alerts.values())
      .filter(alert => alert.status.state === 'pending');

    for (const alert of pendingAlerts) {
      try {
        await this.processAlert(alert);
      } catch (error) {
        console.error(`Error processing alert ${alert.id}:`, error);
        alert.status.state = 'failed';
        alert.status.failureReason = error instanceof Error ? error.message : 'Unknown error';
        alert.status.updatedAt = Date.now();
      }
    }

    this.saveAlerts();
  }

  private async processAlert(alert: ActionableAlert): Promise<void> {
    // Find matching workflow
    const workflow = await this.findMatchingWorkflow(alert);
    
    if (workflow) {
      await this.executeWorkflow(alert.id, workflow);
    } else {
      // No workflow found, create ad-hoc actions
      await this.executeAdHocActions(alert);
    }
  }

  private async findMatchingWorkflow(alert: ActionableAlert): Promise<Workflow | null> {
    const matchingWorkflows = Array.from(this.workflows.values())
      .filter(w => 
        w.isActive && 
        w.trigger.alertType === alert.type &&
        (!w.trigger.priority || w.trigger.priority === alert.priority)
      );

    // Sort by priority and usage
    matchingWorkflows.sort((a, b) => {
      const priorityScore = (b.successRate * 0.7) + (b.usageCount * 0.3);
      const aPriorityScore = (a.successRate * 0.7) + (a.usageCount * 0.3);
      return bPriorityScore - aPriorityScore;
    });

    return matchingWorkflows.length > 0 ? matchingWorkflows[0] : null;
  }


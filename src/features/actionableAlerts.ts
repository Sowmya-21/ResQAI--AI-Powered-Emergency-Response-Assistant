  private async executeWorkflowSteps(workflow: Workflow, execution: WorkflowExecution): Promise<void> {
    for (const step of workflow.steps) {
      try {
        await this.executeStep(step, execution);
      } catch (error) {
        execution.results[step.id] = {
          status: 'failed',
          error: error instanceof Error ? error.message : 'Unknown error',
          startedAt: Date.now(),
          completedAt: Date.now(),
          duration: 0,
          attempts: 1
        };
        this.emit('workflowStepFailed', { step, execution, error });
      }
    }

    execution.status = 'completed';
    execution.completedAt = Date.now();
    this.saveExecutions();
    this.emit('workflowExecutionCompleted', execution);
  }

  private async executeStep(step: WorkflowStep, execution: WorkflowExecution): Promise<void> {
    const stepStartTime = Date.now();
    
    // Check conditions
    const conditionsMet = await this.evaluateStepConditions(step, execution);
    if (!conditionsMet) {
      execution.results[step.id] = {
        status: 'skipped',
        startedAt: stepStartTime,
        completedAt: Date.now(),
        duration: 0,
        attempts: 1
      };
      return;
    }

    execution.results[step.id] = {
      status: 'pending',
      startedAt: stepStartTime
    };

    // Execute actions
    for (const action of step.actions) {
      try {
        await this.executeAction(action, execution);
        execution.results[action.id] = {
          status: 'completed',
          result: 'Action completed successfully',
          startedAt: stepStartTime,
          completedAt: Date.now(),
          duration: Date.now() - stepStartTime,
          attempts: 1
        };
      } catch (error) {
        execution.results[action.id] = {
          status: 'failed',
          error: error instanceof Error ? error.message : 'Unknown error',
          startedAt: stepStartTime,
          completedAt: Date.now(),
          duration: Date.now() - stepStartTime,
          attempts: 1
        };
      }
    }

    // Check success criteria
    const stepSuccess = await this.evaluateStepSuccess(step, execution);
    if (stepSuccess) {
      execution.results[step.id].status = 'completed';
    } else {
      execution.results[step.id].status = 'failed';
    }
  }

  private async evaluateStepConditions(step: WorkflowStep, execution: WorkflowExecution): Promise<boolean> {
    if (!step.conditions || step.conditions.length === 0) {
      return true;
    }

    const alert = this.alerts.get(execution.alertId);
    if (!alert) return false;

    for (const condition of step.conditions) {
      const conditionMet = await this.evaluateCondition(condition, alert, execution);
      if (!conditionMet) {
        return false;
      }
    }

    return true;
  }


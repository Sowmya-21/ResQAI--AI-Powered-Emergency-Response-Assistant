/**
 * Actionable Alerts
 * Response workflows and actionable alert management
 */

export interface ActionableAlert {
  id: string;
  type: 'emergency' | 'safety' | 'health' | 'security' | 'system' | 'user_action' | 'reminder';
  priority: 'critical' | 'high' | 'medium' | 'low' | 'info';
  title: string;
  message: string;
  description: string;
  actions: Action[];
  context: {
    userId?: string;
    sessionId?: string;
    location?: {
      lat: number;
      lng: number;
      address?: string;
    };
    device?: {
      type: 'mobile' | 'tablet' | 'desktop' | 'wearable';
      batteryLevel: number;
      connectivity: string;
    };
    time: {
      timestamp: number;
      urgency: 'immediate' | 'urgent' | 'normal' | 'low';
      deadline?: number;
      timeRemaining?: number;
    };
    environment: {
      weather?: string;
      lighting?: string;
      noiseLevel?: string;
      connectivity?: string;
    };
  };
  status: {
    state: 'pending' | 'in_progress' | 'completed' | 'failed' | 'cancelled' | 'expired';
    createdAt: number;
    updatedAt: number;
    assignedTo?: string;
    assignedAt?: number;
    completedAt?: number;
    completedBy?: string;
    failureReason?: string;
    escalationLevel: number;
  };
  metadata: {
    category: string;
    tags: string[];
    severity: number; // 1-100
    confidence: number; // 0-100
    source: string;
    autoGenerated: boolean;
    expiresAt?: number;
    requiresConfirmation: boolean;
  confirmationCode?: string;
  };
  timestamp: number;
}

export interface Action {
  id: string;
  type: 'primary' | 'secondary' | 'tertiary' | 'emergency' | 'custom';
  title: string;
  description: string;
  icon?: string;
  url?: string;
  method: 'app' | 'web' | 'phone' | 'sms' | 'email' | 'voice' | 'location' | 'device' | 'system';
  parameters?: {
    [key: string]: any;
  };
  conditions: Array<{
    type: 'time' | 'location' | 'device' | 'user' | 'system' | 'context' | 'weather' | 'connectivity';
    field: string;
    operator: 'equals' | 'not_equals' | 'greater_than' | 'less_than' | 'in_range' | 'not_in_range' | 'contains' | 'not_contains';
    value: any;
    caseSensitive?: boolean;
  }>;
  autoExecute?: boolean;
  priority: number; // 1-10
  timeout?: number; // in seconds
  retryPolicy?: {
    maxAttempts: number;
    retryDelay: number; // in seconds
    backoffMultiplier: number;
  };
  successCriteria: {
    type: 'completion' | 'confirmation' | 'response' | 'acknowledgment' | 'dismissal';
    value?: any;
    timeout?: number; // in seconds
  };
  required: boolean;
  order: number; // Execution order
  dependencies: string[]; // Other action IDs that must complete first
}

export interface Workflow {
  id: string;
  name: string;
  description: string;
  version: string;
  isActive: boolean;
  category: 'emergency_response' | 'safety_monitoring' | 'health_management' | 'security_incident' | 'user_assistance' | 'automation';
  trigger: {
    type: 'manual' | 'automatic' | 'scheduled' | 'event_based' | 'condition_based';
    conditions?: Array<{
      field: string;
      operator: string;
      value: any;
    }>;
    alertType?: ActionableAlert['type'];
    priority?: ActionableAlert['priority'];
  };
  actions: Action[];
  steps: WorkflowStep[];
  settings: {
    autoExecute: boolean;
    requireConfirmation: boolean;
    timeout: number; // in seconds
    maxConcurrent: number;
    retryPolicy: {
      maxAttempts: number;
      retryDelay: number;
      backoffMultiplier: number;
    };
    notifications: {
      onCreate: boolean;
      onProgress: boolean;
      onCompletion: boolean;
      onFailure: boolean;
      onTimeout: boolean;
      templates: {
        [key: string]: {
          title: string;
          message: string;
          actions?: string[];
        };
      };
    };
  };
  createdAt: number;
  lastUsed: number;
  usageCount: number;
  successRate: number; // 0-100
  averageCompletionTime: number; // in seconds
}

export interface WorkflowStep {
  id: string;
  workflowId: string;
  name: string;
  description: string;
  type: 'action' | 'decision' | 'notification' | 'delay' | 'condition_check' | 'data_collection' | 'api_call' | 'system_update';
  order: number;
  actions: Action[];
  conditions: Array<{
    field: string;
    operator: string;
    value: any;
  }>;
  timeout?: number; // in seconds
  required: boolean;
  parallel?: boolean;
  dependencies: string[];
  onSuccess?: {
    stepId?: string;
    workflowId?: string;
  };
  onFailure?: {
    stepId?: string;
    workflowId?: string;
    retry?: boolean;
  };
  onTimeout?: {
    stepId?: string;
    workflowId?: string;
  };
  metadata: {
    [key: string]: any;
  };
}

export interface WorkflowExecution {
  id: string;
  workflowId: string;
  alertId: string;
  status: 'pending' | 'running' | 'completed' | 'failed' | 'cancelled' | 'timeout';
  currentStep: number;
  startedAt: number;
  currentStepStartedAt: number;
  completedAt?: number;
  timeoutAt?: number;
  error?: string;
  results: {
    [stepId: string]: {
      status: 'pending' | 'completed' | 'failed' | 'skipped';
      result?: any;
      error?: string;
      startedAt: number;
      completedAt?: number;
      duration?: number;
      attempts: number;
    };
  };
  context: {
    userId?: string;
    sessionId?: string;
    location?: {
      lat: number;
      lng: number;
    };
    device?: {
      type: string;
      capabilities: string[];
    };
  };
  logs: Array<{
    timestamp: number;
    stepId: string;
    level: 'info' | 'warn' | 'error' | 'debug';
    message: string;
    data?: any;
  }>;
}


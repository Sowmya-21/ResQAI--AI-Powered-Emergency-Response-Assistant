  public getSystemStatus(): {
    totalAlerts: number;
    pendingAlerts: number;
    activeWorkflows: number;
    activeExecutions: number;
    successRate: number;
    averageCompletionTime: number;
  } {
    const alerts = Array.from(this.alerts.values());
    const pendingAlerts = alerts.filter(a => a.status.state === 'pending').length;
    const activeWorkflows = Array.from(this.workflows.values()).filter(w => w.isActive).length;
    const activeExecutions = Array.from(this.executions.values()).filter(e => e.status === 'running').length;

    return {
      totalAlerts: alerts.length,
      pendingAlerts,
      activeWorkflows,
      activeExecutions,
      successRate: this.calculateSuccessRate(),
      averageCompletionTime: this.calculateAverageCompletionTime()
    };
  }

  private calculateSuccessRate(): number {
    const completedExecutions = Array.from(this.executions.values())
      .filter(e => e.status === 'completed');

    if (completedExecutions.length === 0) return 0;

    const successCount = completedExecutions.length;
    const totalExecutions = Array.from(this.executions.values()).length;
    
    return Math.round((successCount / totalExecutions) * 100);
  }

  private calculateAverageCompletionTime(): number {
    const completedExecutions = Array.from(this.executions.values())
      .filter(e => e.status === 'completed' && e.completedAt);

    if (completedExecutions.length === 0) return 0;

    const totalTime = completedExecutions.reduce((sum, e) => 
      sum + (e.completedAt! - e.startedAt), 0
    );
    
    return Math.round(totalTime / completedExecutions.length / 1000); // in seconds
  }

  public on(event: string, callback: Function): void {
    if (!this.callbacks.has(event)) {
      this.callbacks.set(event, []);
    }
    this.callbacks.get(event)!.push(callback);
  }

  public off(event: string, callback: Function): void {
    const callbacks = this.callbacks.get(event);
    if (callbacks) {
      const index = callbacks.indexOf(callback);
      if (index > -1) {
        callbacks.splice(index, 1);
      }
    }
  }

  private emit(event: string, data: any): void {
    const callbacks = this.callbacks.get(event);
    if (callbacks) {
      callbacks.forEach(callback => {
        try {
          callback(data);
        } catch (error) {
          console.error(`Error in callback for event ${event}:`, error);
        }
      });
    }
  }

  public cleanup(): void {
    this.callbacks.clear();
    this.alerts.clear();
    this.workflows.clear();
    this.executions.clear();
    this.templates.clear();
  }
